    private void cargarAlertas() {
        try {
            // Establecer conexi√≥n a la base de datos (ajusta las credenciales y URL)
            Connection conn = conn = DatabaseConnection.getConnection();
            Statement statement = conn.createStatement();

            // Obtener las alertas de la base de datos
            ResultSet resultSet = statement.executeQuery("SELECT reserva_id, mensaje FROM alertas");

            // Mostrar las alertas en el JTextArea
            while (resultSet.next()) {
                int reservaId = resultSet.getInt("reserva_id");
                String mensaje = resultSet.getString("mensaje");
                String alerta = "ID de reserva: " + reservaId + " " + mensaje + "\n";
                txtaReservasP.append(alerta);
            }

            // Cerrar recursos
            resultSet.close();
            statement.close();
            conn.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

